            <h4>Le processus pour l'annonceur</h4>
</br>
            <ul class="list-unstyled">
               <li>1. L’ «Annonceur» publie une annonce dans laquelle il indique</li>
               <ul>
                  <li>a) la marchandise à transporter</li>
                  <li>b) le lieu de départ et d’arrivée</li>
                  <li>c) la date de transport (flexible ou non)</li>
               </ul>
                 </br>
               <li>2. L’ «Annonceur» reçoit des offres de «Transporteurs», il choisit et valide l’offre qui lui convient, puis paye un acompte (qui constitue la commission de la société TEEMW), les coordonnées du «Transporteur» lui sont alors communiquées et ses coordonnées sont communiquées au «Transporteur»</li>
  </br>
              <li>3. L’ «Annonceur» et le «Transporteur» entrent en contact via la plateforme pour finaliser le cas échéant les détails du transport</li>
  </br>
                <li>4. La marchandise est livrée et l’«Annonceur» paye le solde de la transaction au «Transporteur»</li>
            </ul>
          </br>
            <h4>Le processus pour le transporteur</h4>
</br>
<ul class="list-unstyled">
   <li>1. Le «Transporteur» reçoit une notification pour toute annonce qui correspond à ses critères</li>
     </br>
   <li>2. Le «Transporteur» soumet un devis via la plateforme à l’annonceur</li>
</br>
  <li>  3. Si son offre est acceptée, le «Transporteur» reçoit une notification</li>
</br>
    <li>4. Lorsque l’annonceur a payé l’acompte, ses coordonnées sont transmises au «Transporteur»</li>
  </br>
      <li> 5. L’«Annonceur» et le «Transporteur» entrent en contact via la plateforme pour finaliser le cas
        échéant les détails du transport</li>
</ul>
</br>
  <h4>Différence par rapport à la concurrence ?</h4>
</br>
<p>  Pas de concurrence. Existe le système inverse et spécialement pour les professionnels (le
  transporteur insère une annonce de transport à vide – bourse au transport) </p>
</br>
  <h4>Nos clients :</h4>
</br>
<ul class="list-unstyled">
   <li>Le particulier qui doit transporter un objet, un véhicule, déménager ou organiser une sortie de classe…</li>
     </br>
     <li>L’entreprise qui doit expédier de la marchandise : sa propre production, une machine pour réparation etc…</li>
       </br>
       <li>Le transporteur qui souhaite rentabiliser ses transports à vide</li>
